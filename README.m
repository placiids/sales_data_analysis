# Sales Data Analysis

## Objective
Analyze sales data using Python and visualize sales trends.

## Tools Used
- Python
- Pandas
- Matplotlib
- Seaborn
- Jupyter Notebook

## Dataset
sales_data_sample.csv

## Tasks Performed
- Loaded the dataset
- Cleaned the data
- Calculated total, average, maximum and minimum sales
- Analyzed product line and territory sales
- Created charts
- Observed sales trends
